class Solution{
  public:
  vector<int>v;
  void inorder(LNode*head)
  {
      LNode*curr=head;
      while(curr)
      {
          v.push_back(curr->data);
          curr=curr->next;
      }
      
  }
    TNode*fun(int s,int e)
    {
        if(s>e)return NULL;
        int m=ceil((float)(s+e)/2);
        TNode*root=new TNode(v[m]);
        root->left=fun(s,m-1);
        root->right=fun(m+1,e);
        return root;
        
    }
    TNode* sortedListToBST(LNode *head) {
      inorder(head);
      return fun(0,v.size()-1);
      
    }
};

